    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        try {
            //ambil baris dimana kursor berada
            int baris = tblRoom.getSelectedRow();
            int id = (Integer)tRoom.getValueAt(baris, 0);
            int no_room = (Integer)tRoom.getValueAt(baris, 1);
            String class_room = (String)tRoom.getValueAt(baris, 2);
            String status = (String)tRoom.getValueAt(baris, 3);
            RoomEditView ftambah = new RoomEditView(this,true,id, no_room, class_room,status);
            ftambah.setVisible(true); 
            refreshData();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        try {
            //ambil baris dimana kursor berada
            int baris = tblRoom.getSelectedRow();
            int id = (Integer)tRoom.getValueAt(baris, 0);
            int no_room = (Integer)tRoom.getValueAt(baris, 1);
            Object pilihan[] = {"Ya","Tidak"};
            int jawaban = JOptionPane.showOptionDialog(
                    null, 
                    "Apakah data dengan ID "+id+ " dengan Nomor Kamar "+no_room+" akan dihapus?",
                    "Apakah anda yakin",JOptionPane.DEFAULT_OPTION,JOptionPane.WARNING_MESSAGE,null,pilihan,pilihan[0]
            );
            
            if(jawaban==0){
                db.delete_room(id);
            }
            refreshData();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_deleteButtonActionPerformed
    
    
    
    

